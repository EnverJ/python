# Boolean expresssion
# Boolean exp - that result in True or False
print(10 > 2)

#  comparison operators - result in True or False

#  < > =< >= == !=
x = 10
y = 20
print(x > y)
print(x < y)
print(x == y)
print(x != y)

#  Logical operators
#  and | or | not
#  and True f both exp are true
#  or True if any one exp is true
#  not True if exp is false
x = 10
y = 20
z = 30
print(x < y and z > y)  # True
print(x < y or x < z)  # True
print(not (x < y))  # False
